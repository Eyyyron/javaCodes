package firstYear.prelims;
import java.lang.*;

public class HundredTimesForLoop {
    public static void main(String[] args) {

        //For-loop
        for (int n = 0; n <= 100; n++) { //Initialization, Loop Condition, and Step Expression
            System.out.println("Aaron Kyle Aguilar"); //Statement 1

        }//End of for-loop statement

        System.exit(0);//End the program

    }//End of the main method
}//End of the class HundredTimesForLoop


